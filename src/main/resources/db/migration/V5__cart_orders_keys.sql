-- Phase 4 : panier, commandes, clés d'activation, codes promo

CREATE TABLE promo_codes (
    id                UUID PRIMARY KEY,
    code              VARCHAR(50) NOT NULL UNIQUE,
    discount_type     VARCHAR(20) NOT NULL,
    discount_value    DECIMAL(10, 2) NOT NULL,
    min_order_amount  DECIMAL(10, 2),
    max_usages        INT,
    usage_count       INT NOT NULL DEFAULT 0,
    expires_at        TIMESTAMP,
    active            BOOLEAN NOT NULL DEFAULT TRUE,
    created_at        TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE cart_items (
    id          UUID PRIMARY KEY,
    user_id     UUID REFERENCES users(id) ON DELETE CASCADE,
    session_id  VARCHAR(64),
    game_id     UUID NOT NULL REFERENCES games(id) ON DELETE CASCADE,
    quantity    INT NOT NULL DEFAULT 1 CHECK (quantity >= 1 AND quantity <= 10),
    created_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT cart_owner_check CHECK (user_id IS NOT NULL OR session_id IS NOT NULL)
);

CREATE INDEX idx_cart_items_session ON cart_items (session_id);
CREATE INDEX idx_cart_items_user ON cart_items (user_id);
CREATE INDEX idx_cart_items_game ON cart_items (game_id);

CREATE TABLE orders (
    id                         UUID PRIMARY KEY,
    order_number               VARCHAR(32) NOT NULL UNIQUE,
    user_id                    UUID NOT NULL REFERENCES users(id),
    status                     VARCHAR(20) NOT NULL,
    subtotal                   DECIMAL(10, 2) NOT NULL,
    discount_amount            DECIMAL(10, 2) NOT NULL DEFAULT 0,
    total_amount               DECIMAL(10, 2) NOT NULL,
    payment_method             VARCHAR(20),
    stripe_payment_intent_id   VARCHAR(255),
    promo_code_id              UUID REFERENCES promo_codes(id),
    billing_first_name         VARCHAR(100),
    billing_last_name          VARCHAR(100),
    billing_email              VARCHAR(255),
    billing_phone              VARCHAR(50),
    billing_address            VARCHAR(255),
    billing_postal_code        VARCHAR(20),
    billing_city               VARCHAR(100),
    billing_country            VARCHAR(2),
    created_at                 TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at                 TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_orders_user ON orders (user_id);
CREATE INDEX idx_orders_number ON orders (order_number);
CREATE INDEX idx_orders_status ON orders (status);

CREATE TABLE license_keys (
    id           UUID PRIMARY KEY,
    game_id      UUID NOT NULL REFERENCES games(id) ON DELETE CASCADE,
    key_value    VARCHAR(255) NOT NULL,
    status       VARCHAR(20) NOT NULL DEFAULT 'AVAILABLE',
    order_id     UUID REFERENCES orders(id),
    assigned_at  TIMESTAMP
);

CREATE INDEX idx_license_keys_game_status ON license_keys (game_id, status);

CREATE TABLE order_items (
    id              UUID PRIMARY KEY,
    order_id        UUID NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
    game_id         UUID NOT NULL REFERENCES games(id),
    license_key_id  UUID REFERENCES license_keys(id),
    unit_price      DECIMAL(10, 2) NOT NULL,
    quantity        INT NOT NULL DEFAULT 1
);

CREATE INDEX idx_order_items_order ON order_items (order_id);

